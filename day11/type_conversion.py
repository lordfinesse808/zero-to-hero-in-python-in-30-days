integer = 123
print("integer:", integer)
print("type(integer):", type(integer))

string = str(integer)
print("string:", string)
print("type(string):", type(string))

float_str = "3.14"
print("float_str:", float_str)
print("type(float_str):", type(float_str))

float_num = float(float_str)
print("float_num:", float_num)
print("type(float_num):", type(float_num))